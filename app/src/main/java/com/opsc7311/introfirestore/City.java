package com.opsc7311.introfirestore;

public class City {
    public String City;
    public int Day1;
    public int Day2;
    public int Day3;
    public int Day4;

    public City()
    {

    }

    public City(String city, int day1, int day2, int day3, int day4, int day5) {
        City = city;
        Day1 = day1;
        Day2 = day2;
        Day3 = day3;
        Day4 = day4;
        Day5 = day5;
    }

    public String getCity() {
        return City;
    }

    public void setCity(String city) {
        City = city;
    }

    public int getDay1() {
        return Day1;
    }

    public void setDay1(int day1) {
        Day1 = day1;
    }

    public int getDay2() {
        return Day2;
    }

    public void setDay2(int day2) {
        Day2 = day2;
    }

    public int getDay3() {
        return Day3;
    }

    public void setDay3(int day3) {
        Day3 = day3;
    }

    public int getDay4() {
        return Day4;
    }

    public void setDay4(int day4) {
        Day4 = day4;
    }

    public int getDay5() {
        return Day5;
    }

    public void setDay5(int day5) {
        Day5 = day5;
    }

    public int Day5;


}
